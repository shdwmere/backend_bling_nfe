/* eslint-disable @typescript-eslint/no-require-imports */
const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
require('dotenv').config();

const blingRoutes = require('../routes/bling');

const app = express();
const PORT = process.env.PORT || 3001;

// Middlewares de seguranÃ§a
app.use(helmet());

// CORS - configurar origens permitidas
const allowedOrigins = process.env.ALLOWED_ORIGINS ? 
  process.env.ALLOWED_ORIGINS.split(',') : 
  ['http://localhost:5173', 'http://localhost:3000'];

app.use(cors({
  origin: function (origin, callback) {
    // Permite requests sem origin (mobile apps, Postman, etc.)
    if (!origin) return callback(null, true);
    
    if (allowedOrigins.indexOf(origin) !== -1) {
      callback(null, true);
    } else {
      callback(new Error('NÃ£o permitido pelo CORS'));
    }
  },
  credentials: true
}));

// Parser JSON
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true }));

// Health check
app.get('/', (req, res) => {
  res.json({ 
    message: 'Bling NFe API Backend',
    status: 'running',
    version: '1.0.0',
    timestamp: new Date().toISOString()
  });
});

app.get('/health', (req, res) => {
  res.json({ 
    status: 'healthy',
    uptime: process.uptime(),
    timestamp: new Date().toISOString()
  });
});

// Rotas da API Bling
app.use('/api/bling', blingRoutes);

// Middleware de tratamento de erros
app.use((err, req, res, next) => {
  console.error('Erro nÃ£o tratado:', err);
  
  res.status(err.status || 500).json({
    error: 'Erro interno do servidor',
    message: process.env.NODE_ENV === 'development' ? err.message : 'Algo deu errado',
    ...(process.env.NODE_ENV === 'development' && { stack: err.stack })
  });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({
    error: 'Rota nÃ£o encontrada',
    path: req.originalUrl,
    method: req.method
  });
});

// Start server
app.listen(PORT, () => {
  console.log(`ğŸš€ Servidor rodando na porta ${PORT}`);
  console.log(`ğŸ“ Health check: http://localhost:${PORT}/health`);
  console.log(`ğŸ”— API Bling: http://localhost:${PORT}/api/bling`);
  console.log(`ğŸŒ Ambiente: ${process.env.NODE_ENV || 'development'}`);
});

module.exports = app;